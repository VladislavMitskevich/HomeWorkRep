package hw03.math_statistics.custom_comparison;

public class CustomComparison {
   public static boolean isEven(int n) {
        return n % 2 == 0;
    }
   public static boolean isOdd(int n) {
        return n % 2 != 0;
    }
    public static boolean isEqualZero(int n) {
        return n == 0;
    }
}
